function [ fu ] = fu( u )
    global C;
    
    x1 = u(1);
    y1 = u(2);
    x2 = u(3);
    y2 = u(4);
    
    
    v1x = u(5);
    v1y = u(6);
    v2x = u(7);
    v2y = u(8);
    
    
    fu = zeros(9,9);
    fu(1,5) = 1;
    fu(2,6) = 1;
    fu(3,7) = 1;
    fu(4,8) = 1;
    
   
    fu(5,1) = C * (y1^2 - 2*x1^2) / (x1^2 + y1^2)^2.5;
    fu(5,2) = -C * 3 * x1 * y1 / (x1^2 + y1^2)^2.5;
    
    fu(6,1) = -C * 3 * x1 * y1 / (x1^2 + y1^2)^2.5;
    fu(6,2) = C * (x1^2 - 2*y1^2) / (x1^2 + y1^2)^2.5;
    
    fu(7,3) = -C * (y2^2 - 2*x2^2) / (x2^2 + y2^2)^2.5;
    fu(7,4) = C * 3 * x2 * y2 / (x2^2 + y2^2)^2.5;
    
    fu(8,3) = C * 3 * x2 * y2 / (x2^2 + y2^2)^2.5;
    fu(8,4) = -C * (x2^2 - 2*y2^2) / (x2^2 + y2^2)^2.5;
    

    fu(9,1) = 2*x1 - 2*x2;
    fu(9,2) = 2*y1 - 2*y2;
    fu(9,3) = 2*x2 - 2*x1;
    fu(9,4) = 2*y2 - 2*y1;
   
    fu(9,9) = 0.007;
    
end


