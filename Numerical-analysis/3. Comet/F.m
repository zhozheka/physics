function [ Rt ] = F( R )

    m = [1.9891*10^30; 
        5.9736*10^24;
        7.3477*10^22; 2.2*10^14]; 
    G = 6.67300*10^(-11);
    
    r = [ R(:, 1), R(:, 2) ];
    v = [ R(:, 3), R(:, 4) ];
    
    
    a(1,:) = G *(m(2)*(r(2,:)-r(1,:)) / norm(r(2,:)-r(1,:))^3 + ...
                 m(3)*(r(3,:)-r(1,:)) / norm(r(3,:)-r(1,:))^3 + ...
                 m(4)*(r(4,:)-r(1,:)) / norm(r(4,:)-r(1,:))^3);
    
    a(2,:) = G *(m(1)*(r(1,:)-r(2,:)) / norm(r(1,:)-r(2,:))^3 + ...
                 m(3)*(r(3,:)-r(2,:)) / norm(r(3,:)-r(2,:))^3 + ...
                 m(4)*(r(4,:)-r(2,:)) / norm(r(4,:)-r(2,:))^3);
    
    a(3,:) = G *(m(2)*(r(2,:)-r(3,:)) / norm(r(2,:)-r(3,:))^3 + ...
                 m(1)*(r(1,:)-r(3,:)) / norm(r(1,:)-r(3,:))^3 + ...
                 m(4)*(r(4,:)-r(3,:)) / norm(r(4,:)-r(3,:))^3);
                 
    a(4,:) = G *(m(2)*(r(2,:)-r(4,:)) / norm(r(2,:)-r(4,:))^3 + ...
                 m(3)*(r(3,:)-r(4,:)) / norm(r(3,:)-r(4,:))^3 + ...
                 m(1)*(r(1,:)-r(4,:)) / norm(r(1,:)-r(4,:))^3);
    
    Rt = [v, a];
 end

